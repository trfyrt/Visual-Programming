/*Sinta memiliki sebuah celengan yang ia isi setiap hari dengan uang Rp 5.000. Buatlah
program yang menghitung jumlah total uang di celengan setelah 30 hari!*/

main(){
  int pemasukan = 5000;
  int hari = 30;

  print("Pemasukan per hari: $pemasukan");
  print("Jumlah hari: $hari");

  print(pemasukan * hari);

}